import cv2
src = cv2.imread("D:\\photo\\20200330153519.jpg")
src = cv2.resize(src, (400, 400), interpolation=cv2.INTER_CUBIC)
src_gray = cv2.cvtColor(src, cv2.COLOR_BGR2GRAY)
src_gray=cv2.equalizeHist(src_gray)
src_edge = cv2.GaussianBlur(src_gray, (15,15) ,0)

#src_edge = cv2.medianBlur(src_gray , 3)

result = cv2.Canny(src_edge, 0, 100, 3)
contours= cv2.findContours(result,cv2.RETR_EXTERNAL,cv2.CHAIN_APPROX_SIMPLE)
cv2.drawContours(src, contours, -1, (255, 0, 0), 2)

cv2.namedWindow('src_gray', cv2.WINDOW_NORMAL)
cv2.resizeWindow("src_gray", 540 , 856)
cv2.moveWindow("src_gray", 200, 100)
cv2.imshow("src_gray", src_gray)
cv2.waitKey(0)
cv2.namedWindow('src_edge', cv2.WINDOW_NORMAL)
cv2.resizeWindow("src_edge", 300, 400)
cv2.moveWindow("src_edge", 200, 100)
cv2.imshow("src_edge", src_edge)

cv2.waitKey(0)
cv2.namedWindow('result', cv2.WINDOW_NORMAL)
cv2.resizeWindow("result", 300, 400)
cv2.moveWindow("result", 200, 100)
cv2.imshow("result", result)
